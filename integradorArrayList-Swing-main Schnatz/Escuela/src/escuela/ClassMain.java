    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
        // TODO add your handling code here:
        Estudiante estudiante = new Estudiante(idEstudiante.getText(),
                nombreEstudiante.getText(), curso.getText(),
                especialidad.getText());
        
        //Agregar al estudiante a la lista
        listaEstudiante.add(estudiante);
        
        //Mostrar detalles del estudiante
        mostrarDetallesEstudiante();
        
    }//GEN-LAST:event_btnAgregarActionPerformed

    private void tablaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tablaMouseClicked
        
        row = tabla.getSelectedRow();
        idEstudiante.setText(dtm.getValueAt(row, 0).toString());
        nombreEstudiante.setText(dtm.getValueAt(row, 1).toString());
        curso.setText(dtm.getValueAt(row, 2).toString());
        especialidad.setText(dtm.getValueAt(row, 3).toString());
        // Llena los inputs de texto con los valores de la fila seleccionada
    }//GEN-LAST:event_tablaMouseClicked

    private void btnRefrescarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRefrescarActionPerformed
           idEstudiante.setText("");
           nombreEstudiante.setText("");
           curso.setText("");
           especialidad.setText("");
           //limpia los campos de texto
    }//GEN-LAST:event_btnRefrescarActionPerformed

    private void btnBorrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBorrarActionPerformed
        int opt = JOptionPane.showConfirmDialog(null, "¿Seguro que querés borrar este estudiante?", "Borrar", JOptionPane.YES_NO_OPTION);
        if(opt == 0){
            dtm.removeRow(row);
            listaEstudiante.remove(row);
            mostrarDetallesEstudiante();
            //pregunta si se quiere borrar el estudiante y en caso afirmativo se elimina
        }
    }//GEN-LAST:event_btnBorrarActionPerformed

    private void btnActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualizarActionPerformed
        listaEstudiante.get(row).setId(idEstudiante.getText());
        listaEstudiante.get(row).setNombre(nombreEstudiante.getText());
        listaEstudiante.get(row).setCurso(curso.getText());
        listaEstudiante.get(row).setEspecialidad(especialidad.getText());
        mostrarDetallesEstudiante();
        //actualiza la fila seleccionada con los valores de los campos 
    }//GEN-LAST:event_btnActualizarActionPerformed

