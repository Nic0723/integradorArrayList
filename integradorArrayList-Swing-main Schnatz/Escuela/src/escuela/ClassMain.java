    private void mostrarDetallesEstudiante() {
    dtm.setRowCount(0);
    
    for(int i = 0; i < listaEstudiante.size();i++){
        
        Object[] obj = {
            listaEstudiante.get(i).getId(),
            listaEstudiante.get(i).getNombre(),
            listaEstudiante.get(i).getCurso(),
            listaEstudiante.get(i).getEspecialidad()
        };
        dtm.addRow(obj);
    
    }

    }
